    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        try {

            Usuario user = new Usuario();

            user.setNome(request.getParameter("Username"));
            user.setSenha(request.getParameter("Password"));

            UsuarioDao uDao = new UsuarioDao(user);

            user = uDao.isValid();

            if (user.getId() != null) {
                HttpSession session = request.getSession(true);
                session.setAttribute("currentSessionUser", user);
                response.sendRedirect("default/modelo.jsp"); //logged-in page    
            } else {
                response.sendRedirect("../index.jsp?usuarioValido=false"); //error page 
            }
        } catch (Exception e) {
            System.out.println("<h2>Ooops... Erro: " + e.getMessage() + "</h2>");
        }

    }

